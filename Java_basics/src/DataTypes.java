public class DataTypes {
    public static void main(String[] args) {
        System.out.println("Below are the 8 primitive datatypes in Java");
        boolean a = true;
        System.out.println(a);
        char c = 'b';//for characters
        System.out.println(c);
        byte b = 2;//for bytes
        System.out.println(b);
        short s = 3;//for short can print -32768-32767
        System.out.println(s);
        int i =45;//for  integer can print  -2,147,483,648 - 2,147,483,647.
        System.out.println(i);
        long l = 656546544565464555l;//for bigger numbers such as mobile number
        System.out.println(l);
        float f = 54564.6565465f;//less precise-to print decimals 32-bit
        System.out.println(f);
        double d = 565.456986;//more precise-to print decimals 64-bit
        System.out.println(d);

    }
}
